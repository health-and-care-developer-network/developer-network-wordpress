                    <h1>The path through assessment on the Digital Tools Library</h1>

                    <p>The current NHS Digital Tools Library (<a href="https://apps.beta.nhs.uk">apps.beta.nhs.uk</a>) is a beta site, which means that it is in development. It has also been referred to as the Digital Apps Library.</p>
                    <p>The library holds a list of digital tools (health apps and web sites) that will be graded / badged in some way. The tools will be monitored for functional changes and grouped in categories that reflect their overall function; for example, mental health, diabetes.</p>
                    <p>Citizens can access an expanding set of assessed tools that will help them to organise and manage their own health and care. The library features digital tools that have passed a set of digital assessment questions to assess digital services to a high standard.</p>
                    <p>We have developed a collection of standards and guidance information to help you develop digital tools which meet the principles of quality applied to all digital products. This means your digital tool will meet what we consider to be good quality digital standards for safety, experience and effectiveness. The information is currently being expanded and rationalised to make it useful to all apps developers, including digital tools that will connect to NHS national systems for data access (connected apps/ services).</p>
                    <p>All digital tools applying to go on the NHS Digital Tools Library must be assessed equally and thoroughly through the current process framework or, later, the evolved national assessment process that replaces this towards the end of 2017. If your digital tool has already been given some form of NHS approval (or some level of endorsement or review) this may be considered by the relevant NHS body that previously reviewed your app. The key point is that the national assessment process includes <strong>all</strong> NHS bodies to cover every aspect of regulation, from data protection to clinical safety and efficacy.</p>

